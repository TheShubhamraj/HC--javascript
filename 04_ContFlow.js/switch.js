const month = 4;

switch (month) {
    case 1:
        console.log("Janu");
        
        break;
    case 2:
        console.log("Feb");
        
        break;
    case 3:
        console.log("march");
        
        break;
    case 4:
        console.log("April");
        
        break;
        

    default:
        console.log("Invalid month");
        
        break;
}